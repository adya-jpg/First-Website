public class print_statements { 
    public static void main (String[] args)
    {
        //this comment style is one line only.
        
        /* this is a multi-line comment style.
         * - adya verma. */

         //is honestly easier to type out.
         
         //prints on the same line.
         System.out.println(" ");
         System.out.println(" ");
         System.out.print("hello, robotics.");
         System.out.print(" my name is adya verma."); 

         //the println function prints on a new line.
         System.out.println(" ");
         System.out.println(" ");
         System.out.println("hello, robotics.");
         System.out.println("my name is adya verma."); 

         //to modify the original print statement to print on a new line, add \n.
         System.out.println(" ");
         System.out.println(" ");
         System.out.print("hello, robotics.\nmy name is adya verma.");
         System.out.println(" ");
        
    }

}